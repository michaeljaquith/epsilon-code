def fig_token_focus_detail():
    print("[fig_token_focus_detail] per-token trajectory")

    eps = _stripe_token_trajectory()
    n = len(eps)
    x = np.arange(n)

    fig, ax = plt.subplots(figsize=(W_DOUBLE, 3.2))

    # Two colours: flagged (>= 0.30) vs confident background.
    # Paused tier (>= 0.95) is shown separately for the peak bar only.
    colors = ['#D65F5F' if e >= 0.95
              else '#F5A623' if e >= 0.30
              else '#BBBBBB'
              for e in eps]
    ax.bar(x, eps, color=colors, edgecolor='none', width=0.9)

    # Only the flag threshold line — 0.65 no longer exists as a tier boundary.
    ax.axhline(0.30, color=C_THRESH_SOFT, linestyle='--', linewidth=1.0,
               label=r'flag threshold ($\varepsilon = 0.30$)')

    # Annotate the two most uncertain tokens (the method-selector decision).
    # Trigger at 0.70 (the accumulation floor) — no tier label attached.
    annotated = [(i, e) for i, e in enumerate(eps) if e >= 0.70]
    for i, e in annotated:
        label = ('.Charge / .PaymentIntent' if i == 22
                 else 'adjacent selector')
        # Place labels to the right and above, away from the upper-left legend.
        ax.annotate(f'{label}\nε={e:.3f}',
                    xy=(i + 0.5, e),
                    xytext=(min(i + 12, n - 14), e + 0.10),
                    fontsize=FS_ANNOT - 0.5,
                    ha='left', va='bottom', color='#222',
                    arrowprops=dict(arrowstyle='->', color='#444',
                                    linewidth=0.7))

    ax.set_xlim(-1, n)
    ax.set_ylim(0, 1.15)
    ax.set_xlabel(f'token position (of {n} retained semantic tokens)',
                  fontsize=FS_LABEL)
    ax.set_ylabel(r'$\varepsilon_t$', fontsize=FS_LABEL)

    summary = (f'total retained tokens: {n}\n'
               f'above flag (0.30): 10  ({10/n*100:.1f}%)\n'
               f'above 0.70: 2  ({2/n*100:.1f}%)')
    ax.text(0.02, 0.98, summary,
            transform=ax.transAxes, fontsize=FS_ANNOT - 0.5,
            va='top', ha='left',
            bbox=dict(facecolor='white', edgecolor='#AAA', linewidth=0.5))

    # Legend placed bottom-right, well clear of the annotations above.
    ax.legend(loc='lower right', frameon=False, fontsize=FS_ANNOT)

    fig.tight_layout()
    _save(fig, 'fig_token_focus_detail.pdf')


