# Fig token_focus (teaser): heat strip of per-token eps
# v5: legend moved off-axis, labels spread with leader lines, larger font
# --------------------------------------------------------------------------

def fig_token_focus():
    print("[fig_token_focus] teaser heat strip (v5: improved readability)")

    eps = _stripe_token_trajectory()
    n = len(eps)

    # v5: taller canvas so labels above the strip don't collide with
    # the title, and the colorbar below has room to breathe.
    fig, ax = plt.subplots(figsize=(W_DOUBLE, 2.6))

    cmap = plt.get_cmap('Reds')
    norm = plt.Normalize(vmin=0.0, vmax=1.0)

    # Draw the heat strip cells
    for i, e in enumerate(eps):
        ax.add_patch(plt.Rectangle((i, 0), 1, 1,
                                   facecolor=cmap(norm(e)),
                                   edgecolor='white', linewidth=0.3))

    # v5: mark all tokens above the flag threshold; spread their labels
    # horizontally above the strip with leader lines so none overlap.
    flagged = [(i, e) for i, e in enumerate(eps) if e >= 0.30]

    # Spread label x-positions evenly across the plot width to avoid
    # overlap. The original token position is preserved via a leader.
    if flagged:
        label_xs = np.linspace(3, n - 3, len(flagged))
    else:
        label_xs = []

    for (token_i, e), label_x in zip(flagged, label_xs):
        # Outline the flagged cell for visibility
        ax.add_patch(plt.Rectangle((token_i, 0), 1, 1,
                                   fill=False, edgecolor='black',
                                   linewidth=0.9))
        ax.annotate(fr'$\varepsilon={e:.2f}$',
                    xy=(token_i + 0.5, 1.0),
                    xytext=(label_x, 2.05),
                    fontsize=9,
                    ha='center', va='bottom', color='#222',
                    arrowprops=dict(arrowstyle='-', color='#666',
                                    linewidth=0.7,
                                    connectionstyle='arc3,rad=0'))

    ax.set_xlim(0, n)
    ax.set_ylim(-0.2, 2.6)
    ax.set_xticks([0, n // 4, n // 2, 3 * n // 4, n])
    ax.set_yticks([])
    ax.set_xlabel(f'token position (n = {n} retained semantic tokens)',
                  fontsize=FS_LABEL, labelpad=8)
    ax.set_title('Stripe Scenario A (GPT-4o): '
                 r'10 tokens exceed $\varepsilon\geq 0.30$',
                 fontsize=FS_BASE - 0.5, pad=10)
    ax.grid(visible=False)
    for spine in ('top', 'right', 'left'):
        ax.spines[spine].set_visible(False)

    # v5: colorbar anchored clearly below the strip so its tick labels do
    # not collide with the x-axis label.
    sm = plt.cm.ScalarMappable(cmap=cmap, norm=norm)
    sm.set_array([])
    cbar = fig.colorbar(sm, ax=ax, orientation='horizontal',
                        fraction=0.05, pad=0.45, aspect=40)
    cbar.set_label(r'per-token $\varepsilon$', fontsize=FS_ANNOT)
    cbar.ax.tick_params(labelsize=FS_ANNOT - 1)

    fig.tight_layout()
